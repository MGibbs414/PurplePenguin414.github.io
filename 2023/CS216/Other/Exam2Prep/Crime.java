package CS216.Other.Exam2Prep;

public class Crime {

    String code;
    String description;
    String level;


    public Crime()
    {
        code = " ";
        description = " ";
        level = " ";
    }


    public Crime(String c, String d, String l)
    {
        code = c;
        description = d;
        level = l;
    }


}
